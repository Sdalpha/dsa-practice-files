package Codechef;

import java.util.*;

class BUYPLSE {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int x = sc.nextInt();
        int y = sc.nextInt();
        int result = (a*x) + (b*y);
        sc.close();
        System.out.println(result);
    }
    
}